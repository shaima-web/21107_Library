    private void searchclientKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchclientKeyReleased
        // TODO add your handling code here:
        ClientDao cdao = new ClientDao();
        List<Client> clients = cdao.findById(searchclient.getText());
        for(Client c:clients){
            regid.setText(c.getRegid());
            fname.setText(c.getFirstname());
            lname.setText(c.getLastname());
            phone.setText(c.getPhoneNo());
            email.setText(c.getEmailAddress());
            catcombo.setSelectedItem(c.getClientCategory());
            path = c.getImage();
            imgcon = new ImageIcon(path);
            Image img = imgcon.getImage().getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
            photo.setIcon(new ImageIcon(img));
        }
    }//GEN-LAST:event_searchclientKeyReleased

