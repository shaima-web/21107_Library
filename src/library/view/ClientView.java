    private void populateClient(){
        String[] colnames = {"Reg Id","F. Name","L. Name","Phone No","Email Address","Category","Photo"};
        DefaultTableModel model = (DefaultTableModel) cltable.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(colnames);
        ClientDao bdao = new ClientDao();
        List<Client> clients = bdao.displayClient();
        cltable.getColumn("Photo").setCellRenderer(new myTableCellRender());
        for(Client b:clients){
            JLabel lbl = new JLabel();
            imgcon = new ImageIcon(b.getImage());
            Image img = imgcon.getImage().getScaledInstance(80, 80, Image.SCALE_DEFAULT);
            lbl.setIcon(new ImageIcon(img));
            Object[] obj = {b.getRegid(),b.getFirstname(),b.getLastname(),b.getPhoneNo(),b.getEmailAddress(),b.getClientCategory(),lbl};
            model.addRow(obj);
        }
        cltable.setModel(model);
    }
    class myTableCellRender implements TableCellRenderer{
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            TableColumn tc = cltable.getColumn("Photo");
            tc.setMaxWidth(80);
            tc.setMinWidth(80);
            table.setRowHeight(60);
            return (Component) value;
        }
    }
