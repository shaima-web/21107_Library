    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Account acc = new Account();
        AccountDao ad = new AccountDao();
        decript d = new decript();
        if(passtext.getText().equals(pass2text.getText())){
            acc.setFirstname(fnametext.getText());
            acc.setLasttname(lnametext.getText());
            acc.setUsername(usertext.getText());
            acc.setPassword(d.econde(passtext.getText()));
            acc.setEmail(mailtext.getText());
            acc.setPhone(phonetext.getText());
            ad.createAccount(acc);
            JOptionPane.showMessageDialog(this, "Account created successfully!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

