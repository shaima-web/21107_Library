    private void checkinbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkinbtnActionPerformed
        // TODO add your handling code here:
        if(checkin.isSelected()){
            Operations op = new Operations();
            OperationsDao opdao = new OperationsDao();
            ClientDao cdao = new ClientDao();
            for(int i =0;i<table.getModel().getRowCount();i++){
                op.setOpId(opdao.getOpNumber(new SimpleDateFormat("dd-MM-yy").format(date.getDate().getTime())));
                op.setClient(new Client(cdao.getRegId(name.getText())));
                op.setBook(new Book((String) table.getValueAt(i, 0)));
                op.setDatetime(new Date(date.getDate().getTime()));
                op.setOperationCategory("CHECK IN");
                op.setStatus("Returned");
                opdao.increaseOrDecrease((String) table.getValueAt(i, 0), "CHECK IN");
                opdao.updateOperation(cdao.getRegId(name.getText()), (String) table.getValueAt(i, 0));
                opdao.saveOperation(op);
            }
            JOptionPane.showMessageDialog(this, "Operation Succeed!!");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Invalid Operation");
        }
    }//GEN-LAST:event_checkinbtnActionPerformed

    private void checkoutbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutbtnActionPerformed
        // TODO add your handling code here:
        if(checkout.isSelected()){
            Operations ck = new Operations();
            OperationsDao ckdao = new OperationsDao();
            ClientDao cdao = new ClientDao();
            for(int i =0;i<table.getModel().getRowCount();i++){
                ck.setOpId(ckdao.getOpNumber(new SimpleDateFormat("dd-MM-yy").format(date.getDate().getTime())));
                ck.setClient(new Client(cdao.getRegId(name.getText())));
                ck.setBook(new Book((String) table.getValueAt(i, 0)));
                ck.setDatetime(new Date(date.getDate().getTime()));
                ck.setOperationCategory("CHECK OUT");
                ck.setStatus("Have");
                ckdao.increaseOrDecrease((String) table.getValueAt(i, 0), "CHECK OUT");
                ckdao.saveOperation(ck);
            }
            JOptionPane.showMessageDialog(this, "Operation Succeed!!");
        }else
        {
            JOptionPane.showMessageDialog(this, "Invalid Operation!!");
        }
    }//GEN-LAST:event_checkoutbtnActionPerformed


