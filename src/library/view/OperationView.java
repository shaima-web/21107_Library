    private void searchclKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchclKeyReleased
        // TODO add your handling code here:
        ClientDao cdao = new ClientDao();
        List<Client> clients = cdao.findById(searchcl.getText());
        for(Client c :clients){
            regno.setText(c.getRegid());
            name.setText(c.getFirstname()+" "+c.getLastname());
            String path = c.getImage();
            ImageIcon imgicon = new ImageIcon(path);
            Image image = imgicon.getImage().getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
            photo.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_searchclKeyReleased

