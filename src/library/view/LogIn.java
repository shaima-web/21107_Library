        decript d = new decript();
        String password = d.decode(adao.findByUsername(usertext.getText()));
        if(password.equals(passtext.getText())){
            MainView hm = new MainView();
            hm.show();
            this.hide();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Invalid Password");
        }
